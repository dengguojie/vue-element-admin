def get_tf_func(_op_name: str):
    """
    Get tensorflow function by op_name
    :param _op_name:
    :return:
    """
    import tensorflow as tf
    # noinspection PyUnresolvedReferences
    from tensorflow.python.ops import gen_nn_ops
    # noinspection PyUnresolvedReferences
    from tensorflow.python.ops import gen_state_ops
    # noinspection PyUnresolvedReferences
    from tensorflow.python.ops import gen_array_ops
    # noinspection PyUnresolvedReferences
    from tensorflow.python.ops import gen_math_ops
    # noinspection PyUnresolvedReferences
    from tensorflow.python.ops import nn_impl
    # noinspection PyUnresolvedReferences
    from tensorflow.python.training import training_ops
    # noinspection PyUnresolvedReferences
    from tensorflow.python.ops import gen_bitwise_ops
    # noinspection PyUnresolvedReferences
    from tensorflow.python.ops import gen_image_ops
    tf_func_map = {
    }
    if _op_name in tf_func_map:
        _op_name = tf_func_map[_op_name]
    _result = None
    sources = [tf, tf.math, gen_image_ops,
               gen_nn_ops, tf.nn, gen_state_ops, gen_array_ops, gen_math_ops, nn_impl, training_ops, gen_bitwise_ops]
    for source in sources:
        _result = getattr(source, _op_name, None)
        if _result is not None:
            return _result
    if _result is None and "_d" in _op_name:
        _result = get_tf_func(_op_name.replace("_d", ""))
    if _result is None and "_v2" in _op_name:
        _result = get_tf_func(_op_name.replace("_v2", ""))
    if _result is None and "_v1" in _op_name:
        _result = get_tf_func(_op_name.replace("_v1", ""))
    if _result is None and "_v3" in _op_name:
        _result = get_tf_func(_op_name.replace("_v3", ""))
    if _result is None and "_1d" in _op_name:
        _result = get_tf_func(_op_name.replace("_1d", "1d"))
    if _result is None and "_2d" in _op_name:
        _result = get_tf_func(_op_name.replace("_2d", "2d"))
    if _result is None and "_3d" in _op_name:
        _result = get_tf_func(_op_name.replace("_3d", "3d"))
    if _result is None and "_" in _op_name:
        _result = get_tf_func(_op_name.replace("_", ""))
    return _result


